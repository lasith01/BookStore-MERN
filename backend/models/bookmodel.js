//create book schema
import mongoose from "mongoose";

//create book schema variable
const bookSchema = mongoose.Schema(
    {
        title: {
            type: String,
            required: true,
        },
        author: {
            type: String,
            required: true,
        },
        publishYear: {
            type: Number,
            required: true,
        },
    },
    {
        timestamps: true,
    }
);

//create model
export const Book = mongoose.model('Cat', { name: String });
